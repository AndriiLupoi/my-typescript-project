// Оголошення змінних різних типів
var myString = "Hello, TypeScript!";
var myNumber = 42;
var myBoolean = true;
var myAny = "I can be anything!";
// Виведення значень змінних у консоль
console.log("String:", myString);
console.log("Number:", myNumber);
console.log("Boolean:", myBoolean);
console.log("Any:", myAny);
// Оголошення масивів з конкретними типами
var stringArray = ["One", "Two", "Three"];
var numberArray = [1, 2, 3];
// Виведення масивів у консоль
console.log("\nString Array:", stringArray);
console.log("Number Array:", numberArray);
